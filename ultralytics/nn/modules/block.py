# 1. PCE
class PyramidPoolAgg_PCE(nn.Module):
    def __init__(self, stride=2):
        super().__init__()
        self.stride = stride

    def forward(self, inputs):
        B, C, H, W = inputs[-1].shape
        H = (H - 1) // self.stride + 1
        W = (W - 1) // self.stride + 1
        return torch.cat([nn.functional.adaptive_avg_pool2d(inp, (H, W)) for inp in inputs], dim=1)

class RCAWithAttention(nn.Module):
    def __init__(self, inp, kernel_size=1, ratio=2, band_kernel_size=11, square_kernel_size=3):
        super(RCAWithAttention, self).__init__()
        
        # Depthwise convolution
        self.dwconv_hw = nn.Conv2d(inp, inp, square_kernel_size, padding=square_kernel_size//2, groups=inp)
        
        # Height and Width Pooling
        self.pool_h = nn.AdaptiveAvgPool2d((None, 1))
        self.pool_w = nn.AdaptiveAvgPool2d((1, None))
        
        # Attention mechanism with global context
        gc = inp // ratio
        self.excite = nn.Sequential(
            nn.Conv2d(inp, gc, kernel_size=(1, band_kernel_size), padding=(0, band_kernel_size // 2), groups=gc),
            nn.BatchNorm2d(gc),
            nn.ReLU(inplace=True),
            nn.Conv2d(gc, inp, kernel_size=(band_kernel_size, 1), padding=(band_kernel_size // 2, 0), groups=gc),
            nn.Sigmoid()
        )

        # Self-attention to capture long-range dependencies
        self.query_conv = nn.Conv2d(inp, inp // 8, kernel_size=1)
        self.key_conv = nn.Conv2d(inp, inp // 8, kernel_size=1)
        self.value_conv = nn.Conv2d(inp, inp, kernel_size=1)
        self.gamma = nn.Parameter(torch.zeros(1))

    def sge(self, x):
        # Pool along height and width
        x_h = self.pool_h(x)
        x_w = self.pool_w(x)
        x_gather = x_h + x_w  # Combining pooled features

        # Excitation to get channel-wise attention
        ge = self.excite(x_gather)
        
        return ge

    def self_attention(self, x):
        B, C, H, W = x.size()
        
        # Generate query, key, value
        query = self.query_conv(x).view(B, -1, H * W).permute(0, 2, 1)
        key = self.key_conv(x).view(B, -1, H * W)
        value = self.value_conv(x).view(B, C, H * W)
        
        # Attention map
        attention = torch.bmm(query, key)
        attention = F.softmax(attention, dim=-1)

        # Output with long-range context
        out = torch.bmm(value, attention.permute(0, 2, 1)).view(B, C, H, W)
        out = self.gamma * out + x  # Residual connection
        return out

    def forward(self, x):
        # Depthwise Convolution to get spatial context
        loc = self.dwconv_hw(x)

        # Apply spatial and channel-wise attention
        att = self.sge(x)
        out = att * loc

        # Apply self-attention for long-range dependencies
        out = self.self_attention(out)
        
        return out
    
class PyramidContextExtraction(nn.Module):
    def __init__(self, dim, n=3) -> None:
        super().__init__()
        
        self.dim = dim
        self.ppa = PyramidPoolAgg_PCE()
        self.rcm = nn.Sequential(*[RCAWithAttention(sum(dim), 3, 2, square_kernel_size=1) for _ in range(n)])
        
    def forward(self, x):
        x = self.ppa(x)
        x = self.rcm(x)
        return torch.split(x, self.dim, dim=1)
    
# 2. HWFEM
class HWFEM(nn.Module):
    def __init__(
        self,
        inp: int,
    ) -> None:
        super(HWFEM, self).__init__()

        self.fuse1 = Conv(inp, inp, act=False)
        self.fuse2 = Conv(inp, inp, act=False)
        self.act = h_sigmoid()

    def forward(self, x):
        x_l, x_h = x
        B, C, H, W = x_l.shape
        inp = self.fuse1(x_l)
        sig_act = self.fuse2(x_h)
        sig_act = F.interpolate(self.act(sig_act), size=(H, W), mode='bilinear', align_corners=False)
        out = inp * sig_act
        return out

